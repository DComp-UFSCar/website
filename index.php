        <header>O curso de BCCS</header>
        <div class="text-content">
          <p>A tecnologia da informação impõe o emprego da computação em toda atividade, desde as tecnologias para comunicação, passando para saúde, gestão, artes, ensino e fundamentalmente na pesquisa. Hoje, praticamente, todos os dispositivos elétricos incorporam um processador. Acredita-se que a grande revolução do último século foi o surgimento dos computadores. Do ponto de vista da ciência, o uso dos computadores tem permitido um amplo desenvolvimento em todos os setores, gerando novos resultados até então intangíveis pelas técnicas disponíveis anteriormente.</p>
          <p>De forma geral, um curso de Ciência da Computação destina-se à formação de profissionais capazes de atuar em áreas que exijam a elaboração de algoritmos e sistemas voltados ao atendimento das necessidades específicas de diversas áreas, incluindo aquelas voltadas ao desenvolvimento de infraestrutura de software dos computadores (sistemas operacionais, compiladores, banco de dados, navegadores) e software para sistemas embarcados, móveis, de computação nas nuvens.</p>
          <p>Com essa perspectiva, o Bacharelado em Ciência da Computação do DComp/UFSCar (BCCS) visa preparar um profissional capacitado para o planejamento, análise, pesquisa, desenvolvimento e utilização de sistemas computacionais nas mais diversas atividades da sociedade. Além disso, o profissional formado pelo BCCS deve aliar conhecimento, criatividade e capacidade crítica para atuar como solucionador de problemas da área de Computação, tanto em ambiente empresarial quanto acadêmico, sempre considerando os aspectos essenciais para um desenvolvimento sustentável.</p>
          <p>Em resumo, o BCCS é uma opção inovadora para estudantes que desejam seguir uma carreira profissional na vanguarda da inovação tecnológica e atendendo às premissas da sustentabilidade. O curso tem o objetivo de oferecer um ambiente de aprendizado que possibilite o desenvolvimento de estudantes sem experiência prévia em Computação e, ao mesmo tempo, desafiar aqueles familiarizados com o assunto.</p>
